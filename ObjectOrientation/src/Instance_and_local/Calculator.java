package Instance_and_local;

public class Calculator {
	int num1;
	int num2;
	void add()
	{
		System.out.println("Addition Result is:"+(num1+num2));
	}
	void sub()
	{
		System.out.println("subtraction Result is:"+(num2-num1));
	}
	void mul()
	{
		System.out.println("Multiplication Result is:"+(num1*num2));
	}
	void div()
	{
		System.out.println("Division Result is:"+(num2/num1));
	}
	void rem()
	{
		System.out.println("Remainder is:"+(num2%num1));
	}
}
